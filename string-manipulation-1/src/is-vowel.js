/* exported isVowel */
function isVowel(character) {
  if (character === 'A' || character === 'a' || character === 'E' || character === 'e' || character === 'I' || character === 'o' || character === 'O' || character === 'U' || character === 'u') {
    return true;
  } else {
    return false;
  }
}
